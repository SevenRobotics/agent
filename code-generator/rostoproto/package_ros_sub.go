package rostoproto

import (
	"fmt"
	"path/filepath"
)

type RosSubPackage struct {
	SimpleTarget
}

func NewRosSubPackage(packagePath, packageDir, packageName string, generateAll bool) *RosSubPackage {
	pkg := &RosSubPackage{
		SimpleTarget: SimpleTarget{
			PkgName:       packageName,
			PkgPath:       packagePath,
			PkgDir:        packageDir,
			HeaderComment: []byte("// This file is auto-generated by rostoproto, do not edit Manually!!\n\n\n"),
		},
	}
	pkg.GeneratorsFunc = pkg.generatorsFunc
	return pkg
}

func (p *RosSubPackage) ProtoTypeName() Name {
	return Name{
		Name:    p.Path(), // the ros msg path std_msgs/header
		Package: p.Name(), // the protobuf package std_msgs.header
		Path:    p.ImportPath(),
	}
}

func (p *RosSubPackage) ImportPath() string {
	return filepath.Join(p.Path(), fmt.Sprintf("%s.go", p.Name()))
}

func (p *RosSubPackage) OutputPath() string {
	return filepath.Join(p.Path(), fmt.Sprintf("%s.go", p.Name()))
}

func (p *RosSubPackage) generatorsFunc(c *Context) []Generator {
	generators := []Generator{}

	generators = append(generators, &rosSubGen{
		RosSubGenerator: RosSubGenerator{
			OutputFilename: p.Name(),
		},
		localPackage:    Name{Package: p.Name(), Path: p.Path()},
		localRosPackage: Name{Package: p.Path(), Name: p.RosPackageName()},
		// imports:         *p.Imports,
	})
	// log.Printf("Returning generator for package %v with path %v and ros package name %v\n",
	// 	p.Name(), p.Path(), p.RosPackageName())
	return generators
}

func (p *RosSubPackage) RosPackageName() string {
	return filepath.Base(p.Path())
}

var (
	_ = Target(&RosSubPackage{})
)
